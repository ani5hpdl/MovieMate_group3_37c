    private void PayNowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PayNowActionPerformed
        // TODO add your handling code here:
        util.StripeRedirectListener.startServer(this);
        String url = Controller.CheckoutController.createCheckoutSession();

        if (!url.startsWith("http")) {
            JOptionPane.showMessageDialog(this, "Payment failed: " + url);
        } else {
            try {
                java.awt.Desktop.getDesktop().browse(java.net.URI.create(url));
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Failed to open browser: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_PayNowActionPerformed

